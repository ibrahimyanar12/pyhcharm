birinci_sayı=int(input("birinci sayıyı giriniz: "))
ikinci_sayı=int(input("ikinci sayıyı giriniz: "))
islem=input("""yapmak isteğiniz işlemi giriniz.
(Toplama:+,Çıkarma:-,Çarpma:*,Bölme:/""")
if islem=="+":
    print(birinci_sayı+ikinci_sayı)
elif islem=="-":
    print(birinci_sayı - ikinci_sayı)
elif islem=="*":
    print(birinci_sayı*ikinci_sayı)
elif islem=="/":
    print(birinci_sayı/ikinci_sayı)





